function int2String(array) {
  var result = "";
  for (var i = 0; i < array.length; i++) {
    result += String.fromCharCode(parseInt(array[i], 10));
  }
  return result;
}

function parseHexString(str) { 
    var result = [];
    while (str.length >= 2) { 
        result.push(parseInt(str.substring(0, 2), 16));
        str = str.substring(2, str.length);
    }
    return result;
}

function foldArray(arr) {

    var result = [];
    for (var i = 0; i < arr.length/2; i++) {
        result.push((arr[i] ^ arr[i+arr.length/2]));
    }
    return result;
}

function createHexString(arr) {
    var result = "";
    for (var i in arr) {
        var str = arr[i].toString(16);
        // Pad to two digits, truncate to last two if too long.  Again,
        // I'm not sure what your needs are for the case, you may want
        // to handle errors in some other way.
        str = str.length == 0 ? "00" :
              str.length == 1 ? "0" + str : 
              str.length == 2 ? str :
              str.substring(str.length-2, str.length);
        result += str;
    }

    return result;
}

function generatePassword(entropy, key, domain, username, index) {
    const input_pwd = entropy+'$'+key+'$'+domain+'$'+username+'$'+index;
    const hash = sha256(input_pwd);
    return base85.fromByteArray(parseHexString(hash), false);
}

function generateFoldedPassword(entropy, key, domain, username, index) {
    const input_pwd = entropy+'$'+key+'$'+domain+'$'+username+'$'+index;
    const hash = sha256(input_pwd);
    return base85.fromByteArray(foldArray(parseHexString(hash)), false);
}

function setCookie(name,value,days) {
    var expires = "";
    if (days) {
        var date = new Date();
        date.setTime(date.getTime() + (days*24*60*60*1000));
        expires = "; expires=" + date.toUTCString();
    }
    document.cookie = name + "=" + (value || "")  + expires + "; path=/";
}
function getCookie(name) {
    var nameEQ = name + "=";
    var ca = document.cookie.split(';');
    for(var i=0;i < ca.length;i++) {
        var c = ca[i];
        while (c.charAt(0)==' ') c = c.substring(1,c.length);
        if (c.indexOf(nameEQ) == 0) return c.substring(nameEQ.length,c.length);
    }
    return null;
}
